TileMap::TileMap()
{
	elem1 = TextureManager::LoadTexture("../assets/enemy_temp.png");
	elem2 = TextureManager::LoadTexture("../assets/player_temp.png");

	LoadMap(LV);

	src = new SDL_Rect();

	src->x = 0;
	src->y = 0;
	src->w = IMG_SIZE;
	src->h = IMG_SIZE;

	dest = new SDL_Rect();

	dest->x = 0;
	dest->y = 0;
	dest->w = IMG_SIZE / 10;
	dest->h = IMG_SIZE / 10;
}

TileMap::~TileMap()
{
	SDL_DestroyTexture(elem1);
}

void TileMap::LoadMap(int lv[MAP_HEIGHT][MAP_WIDTH])
{	
	for(int i = 0; i < MAP_HEIGHT; ++i)
		for(int j = 0; j < MAP_WIDTH; ++j)
			map[i][j] = lv[i][j];
}

void TileMap::DrawMap()
{
	int type = 0;

	for(int i = 0; i < MAP_HEIGHT; ++i)
	{
		for(int j = 0; j < MAP_WIDTH; ++j)
		{
			type = map[i][j];

			dest->x = i * (IMG_SIZE / 10);
			dest->y = j * (IMG_SIZE / 10);

			switch(type)
			{
			case 0:
				TextureManager::Draw(elem1, src, dest);
				break;

			case 1:
				TextureManager::Draw(elem2, src, dest);
				break;

			default: break;
			}
		}
	}
}