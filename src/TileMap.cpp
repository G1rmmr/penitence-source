TileMap::TileMap()
{
	elem1 = TextureManager::LoadTexture("../assets/enemy_temp.png");
	elem2 = TextureManager::LoadTexture("../assets/player_temp.png");

	LoadMap(LV);

	src->x = 0;
	src->y = 0;
	src->w = 150;
	src->h = 150;

	dest->x = 0;
	dest->y = 0;
}

TileMap::~TileMap()
{

}

void TileMap::LoadMap(int lv[20][25])
{
	for(int i = 0; i < 20; ++i)
		for(int j = 0; j < 25; ++j)
			map[i][j] = lv[i][j];
}

void TileMap::DrawMap()
{
	int type = 0;

	for(int i = 0; i < 20; ++i)
	{
		for(int j = 0; j < 25; ++j)
		{
			type = map[i][j];

			dest->x = i * 150;
			dest->y = j * 150;

			switch(type)
			{
			case 0:
				TextureManager::Draw(elem1, src, dest);
				break;

			case 1:
				TextureManager::Draw(elem2, src, dest);
				break;

			default: break;
			}
		}
	}
}